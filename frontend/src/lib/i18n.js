export function t(key, vars) {
  const map = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  const template = map[key] ?? TRANSLATIONS.en[key] ?? key;
  if (!vars || typeof template !== 'string') {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : match,
  );
}

export function tCol(col) {
  const key = `col.${col}`;
  const map = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  const translated = map[key] ?? TRANSLATIONS.en[key];
  return translated !== undefined ? translated : col.replace(/_/g, ' ');
}

export function tVal(value) {
  const key = `val.${value}`;
  const map = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  const translated = map[key] ?? TRANSLATIONS.en[key];
  return translated !== undefined ? translated : value;
}
